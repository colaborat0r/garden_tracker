/// Helper: get plants by category
List<PlantInfo> getPlantsByCategory(String category) =>
    masterPlantDatabase.where((p) => p.category == category).toList();

/// Helper: search plants
List<PlantInfo> searchPlants(String query) {
  final q = query.toLowerCase();
  return masterPlantDatabase
      .where((p) =>
          p.commonName.toLowerCase().contains(q) ||
          p.category.toLowerCase().contains(q) ||
          p.commonVarieties.any((v) => v.toLowerCase().contains(q)))
      .toList();
}

